package com.j1902.beststore.modle;

public class ShoppingRecordResult {
    private String name;
    private String img;
    private String time;

    public ShoppingRecordResult() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
